# 100'lük sisteme göre girilen başarı notunu harfli sisteme çeviren programı kodlayınız.

a = int(input("Lütfen Not ortalamanizi giriniz: "))
if a >= 90 :
    print("AA")
elif a >= 80 :
    print("BA")
elif a >= 70:
    print("BB")
elif a >= 60 :
    print("CB")
elif a >= 50:
    print("CC")
else :
    print("FF")
    
